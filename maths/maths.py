def add(a, b):
    """Add two numbers together

    Args:
        a (int): The first number
        b (int): The second number

    Returns:
        int: The sum of both numbers
    """
    return a + b


def subtract(a, b):
    return a - b


def multiply(a, b):
    return a * b


def divide(a, b):
    return a / b


def power(a, b):
    return a ** b